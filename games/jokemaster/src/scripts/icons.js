// Function to replace emoji icons with SVG icons
function initMaterialIcons() {
    document.querySelectorAll('.icon-circle').forEach(iconCircle => {
        const label = iconCircle.closest('.action-icon')?.querySelector('.icon-label')?.textContent;

        let iconSvg = null;
        switch(label) {
            case 'PROJECT':
                iconSvg = Icons.project;
                break;
            case 'CONTACTS':
                iconSvg = Icons.contacts;
                break;
            case 'GLOBE':
                iconSvg = Icons.globe;
                break;
            case 'MAP':
            case 'FLAT MAP':
                iconSvg = Icons.map;
                break;
            case 'JOKES':
                iconSvg = Icons.jokes;
                break;
            case 'BANK':
                iconSvg = Icons.bank;
                break;
        }

        if (iconSvg) {
            iconCircle.innerHTML = iconSvg;
        }
    });
}

// Initialize icons when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initMaterialIcons);
} else {
    initMaterialIcons();
}
